/**
 * Get the current language code.
 * Checks session, then cookie, then browser, defaults to 'en'.
 * 
 * @return string
 */
function getCurrentLanguage() {
    // 1. Session
    if (isset($_SESSION['lang'])) {
        return $_SESSION['lang'];
    }
    
    // 2. Cookie
    if (isset($_COOKIE['gmao_lang'])) {
        return $_COOKIE['gmao_lang'];
    }
    
    // 3. Browser detection
    if (isset($_SERVER['HTTP_ACCEPT_LANGUAGE'])) {
        $browser_lang = substr($_SERVER['HTTP_ACCEPT_LANGUAGE'], 0, 2);
        if (in_array($browser_lang, ['en', 'fr'])) {
            return $browser_lang;
        }
    }
    
    return 'en';
}

/**
 * Set the user's preferred language.
 * Stores in session and long-term cookie.
 * 
 * @param string $language The requested language code ('en' or 'fr').
 * @return bool
 */
function setLanguage($language) {
    if (!in_array($language, ['en', 'fr'])) {
        return false;
    }
    
    $_SESSION['lang'] = $language;
    
    // Cookie valid for 1 year
    setcookie('gmao_lang', $language, time() + (3600 * 24 * 365), '/');
    
    return true;
}